package com.dfire.testcase.function.bean;

public class BaseParamBean {
	
	private String entityId;
	private String seatCode;
	private String orderId;
	
	private String cardId;
	private String xtoken;
	private String waitingOrderId;
	
	public String getEntityId() {
		return entityId;
	}
	public void setEntityId(String entityId) {
		this.entityId = entityId;
	}
	public String getSeatCode() {
		return seatCode;
	}
	public void setSeatCode(String seatCode) {
		this.seatCode = seatCode;
	}
	public String getOrderId() {
		return orderId;
	}
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public String getCardId() {
		return cardId;
	}
	public void setCardId(String cardId) {
		this.cardId = cardId;
	}
	public String getXtoken() {
		return xtoken;
	}
	public void setXtoken(String xtoken) {
		this.xtoken = xtoken;
	}
	public String getWaitingOrderId() {
		return waitingOrderId;
	}
	public void setWaitingOrderId(String waitingOrderId) {
		this.waitingOrderId = waitingOrderId;
	}
	
	
}
